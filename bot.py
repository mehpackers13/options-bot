def ensure_log_file() -> None:
    if not ALERTS_LOG.exists():
        with open(ALERTS_LOG, "w", newline="") as f:
            csv.DictWriter(f, fieldnames=_CSV_HEADERS).writeheader()
        log(f"Created alerts log at {ALERTS_LOG}")


def log_alert(data: dict, signal: dict, confidence: int, is_ep: bool) -> None:
    ensure_log_file()
    c_str = ""
    if data.get("top_contract"):
        c     = data["top_contract"]
        c_str = f"{c['type']} ${c['strike']} {c['expiry']} Vol:{c['volume']:,}"
    row = {
        "timestamp":        datetime.datetime.now(ET).strftime("%Y-%m-%d %H:%M:%S"),
        "ticker":           data["ticker"],
        "signal_type":      signal["type"],
        "signal_label":     signal["label"],
        "signal_value":     signal["value"],
        "threshold_used":   signal["threshold"],
        "price_at_alert":   data["current_price"],
        "put_call_ratio":   data["put_call_ratio"],
        "avg_iv":           data["avg_iv"],
        "total_volume":     data["total_volume"],
        "top_contract":     c_str,
        "confidence_score": confidence,
        "is_earnings_play": int(is_ep),
        "data_source":      data.get("data_source", "yfinance"),
        "outcome":          "",   # fill in: 1=hit, 0=miss
    }
    with open(ALERTS_LOG, "a", newline="") as f:
        csv.DictWriter(f, fieldnames=_CSV_HEADERS).writerow(row)


# ── Market hours ────────────────────────────────────────────────────────────────

def is_market_hours() -> bool:
    now = datetime.datetime.now(ET)
    if now.weekday() >= 5:
        return False
    open_  = now.replace(hour=9,  minute=30, second=0, microsecond=0)
    close_ = now.replace(hour=16, minute=0,  second=0, microsecond=0)
    return open_ <= now < close_


def is_pre_market_window() -> bool:
    now = datetime.datetime.now(ET)
    if now.weekday() >= 5:
        return False
    start = now.replace(hour=8,  minute=0,  second=0, microsecond=0)
    end   = now.replace(hour=9,  minute=25, second=0, microsecond=0)
    return start <= now < end


# ── Main scan ───────────────────────────────────────────────────────────────────

def run_scan() -> None:
    thresholds = load_thresholds()
    log(f"Scanning {len(config.TICKERS)} ticker(s): {', '.join(config.TICKERS)}")

    alerts_fired   = 0
    silent_reasons = []

    for ticker in config.TICKERS:
        log(f"  Checking {ticker} ...")
        data = fetch_options_data(ticker)
        if not data:
            silent_reasons.append(f"{ticker}: data unavailable")
            continue

        # Earnings check (done once per ticker)
        ep, ep_date      = is_earnings_play(ticker)
        near_earn, earn_date = earnings_in_next_week(ticker)
        earnings_soon    = ep  # true = earnings within 24h

        raw_signals = detect_raw_signals(data, thresholds)

        if not raw_signals:
            log(
                f"  {ticker}: no raw signals  "
                f"(PC={data['put_call_ratio']}, IV={data['avg_iv']}%, "
                f"Vol={data['total_volume']:,})"
            )
            continue

        for sig in raw_signals:
            if _is_duplicate(ticker, sig["type"]):
                log(f"  {ticker}: duplicate {sig['type']} suppressed")
                continue

            # Run all four gates
            passes, confidence, reason = check_all_gates(
                ticker        = ticker,
                signal_type   = sig["type"],
                volume_ratio  = sig.get("volume_ratio", 0),
                iv_change_pct = sig.get("iv_change"),
                pc_ratio      = sig.get("pc_ratio"),
                is_earnings_play = ep,
                has_earnings_soon = earnings_soon,
            )

            if not passes:
                log(f"  {ticker} [{sig['type']}] filtered — {reason}")
                silent_reasons.append(f"{ticker} {sig['type']}: {reason}")
                continue

            # All gates passed — fetch context (news + Reddit) only now
            log(f"  *** QUALIFIED SIGNAL: {ticker} — {sig['label']} (confidence {confidence}/100) ***")
            log(f"  Fetching news + sentiment for {ticker} ...")
            context = build_context_summary(ticker)

            # Fire alert
            send_discord_alert(data, sig, confidence, ep, ep_date or earn_date, context)
            log_alert(data, sig, confidence, ep)
            alerts_fired += 1

        time.sleep(1.5)

    if alerts_fired == 0:
        log(
            f"Scan complete — 0 alerts fired. "
            f"{len(silent_reasons)} signal(s) filtered out. "
            f"Silence = discipline."
        )
    else:
        log(f"Scan complete — {alerts_fired} alert(s) sent.")

    send_health_update(
        f"{'✅' if alerts_fired > 0 else '🔕'} "
        f"{alerts_fired} alert(s) fired — {len(silent_reasons)} filtered",
        alerts_fired,
        len(config.TICKERS),
        silent_reasons,
    )


def main() -> None:
    global _morning_analysis_done_date

    log("=" * 60)
    log("Options Alert Bot v2 — selective & disciplined")
    log(f"Tickers  : {', '.join(config.TICKERS)}")
    log(f"Interval : {config.SCAN_INTERVAL_MINUTES} min | market hours 9:30am–4pm ET")
    log("=" * 60)

    # Initialise data source (attempts Robinhood login)
    data_sources.init()
    ensure_log_file()

    if config.DISCORD_WEBHOOK_URL == "YOUR_DISCORD_WEBHOOK_URL_HERE":
        log("NOTE: Discord webhook not configured — alerts print to console", "WARN")

    while True:
        now       = datetime.datetime.now(ET)
        today_str = now.strftime("%Y-%m-%d")

        if is_pre_market_window() and _morning_analysis_done_date != today_str:
            try:
                from self_improve import run_morning_analysis
                run_morning_analysis()
                _morning_analysis_done_date = today_str
            except Exception as exc:
                log(f"Morning analysis failed: {exc}", "ERROR")

        if is_market_hours():
            try:
                run_scan()
            except Exception as exc:
                log(f"Scan error: {exc}", "ERROR")
                traceback.print_exc()
        else:
            log(
                f"Market closed ({now.strftime('%A %H:%M')} ET). "
                f"Next check in {config.SCAN_INTERVAL_MINUTES} min."
            )

        time.sleep(config.SCAN_INTERVAL_MINUTES * 60)


if __name__ == "__main__":
    main()
